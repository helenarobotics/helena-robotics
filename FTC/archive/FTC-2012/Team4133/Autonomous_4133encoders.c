void parkballfront(string vcolor, string vposition)
{
    int vpostile = 1;

    if (vposition =="in")
    {
        vpostile = vpostile + 1;
    }

    moveforward(50, 4100);
    wait1Msec(2000);

    if (vcolor =="red")
    {
        leftturn(50, v90turn);
    }
    else
    {
        rightturn(50, v90turn);
    }
    wait1Msec(2000);

    moveforward(50, vtile * vpostile);
    wait1Msec(2000);

    if (vcolor =="red")
    {
        leftturn(50, v45turn);
    }
    else
    {
        rightturn(50, v45turn);
    }
    wait1Msec(2000);

    moveforward(50, vtile * 3);
    wait1Msec(2000);
}

